    private void btnMarkAttendedActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMarkAttendedActionPerformed
        // TODO add your handling code here:
        String selectedApp = cmbxAppointment.getSelectedItem().toString();
        String selectedID = cmbxPatId.getSelectedItem().toString();
        Boolean attend = c.markAsAttended(selectedApp, selectedID);
        if(attend == true){
            JOptionPane.showMessageDialog(null, "Patient: "+selectedID+" Marked as Attended on: "+selectedApp, "Success", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnMarkAttendedActionPerformed

