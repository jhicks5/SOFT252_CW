    private void btnAppendNotesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAppendNotesActionPerformed
        // TODO add your handling code here:
        String notes = txtAppNotes.getText();
        String patientId = cmbxPatId.getSelectedItem().toString();
        String selectedApp = cmbxAppointment.getSelectedItem().toString();
        Boolean complete = c.appendAppNotes(notes, patientId, selectedApp);
        if (complete == true){
            JOptionPane.showMessageDialog(null, "Notes successfully appended to patient: "+patientId+" Appointment date: "+selectedApp, "Success", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnAppendNotesActionPerformed

    private void formWindowActivated(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowActivated
        // TODO add your handling code here:
        ArrayList<String> patIds = c.getPatientIds();
        for(int i = 0; i < patIds.size(); i++){
            cmbxPatId.addItem(patIds.get(i));
        }
        
        ArrayList<String> medicines = c.getMedicines();
        for(int i = 0; i < medicines.size(); i++){
            cmbxMeds.addItem(medicines.get(i));
        }
    }//GEN-LAST:event_formWindowActivated

    private void cmbxPatIdItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cmbxPatIdItemStateChanged
        // TODO add your handling code here:
        txtHistory.setText("");
        String patHistory = c.getPatHistory(cmbxPatId.getSelectedItem().toString());
        txtHistory.append(patHistory);
        
        cmbxAppointment.removeAllItems();
        ArrayList<String> appoints = c.getPatientApps(cmbxPatId.getSelectedItem().toString());
        for(int i = 0; i < appoints.size(); i++){
            cmbxAppointment.addItem(appoints.get(i));
        }
    }//GEN-LAST:event_cmbxPatIdItemStateChanged

