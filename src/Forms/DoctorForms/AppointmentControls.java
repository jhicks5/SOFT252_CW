    private void btnCreatePrescriptActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreatePrescriptActionPerformed
        // TODO add your handling code here:
        String doctorid = DoctorUser.userID;
        String patientid = cmbxPatId.getSelectedItem().toString();
        String dosage = txtDosage.getText();
        String notes = txtPrescriptNotes.getText();
        String medicine = cmbxMeds.getSelectedItem().toString();
        
        Boolean complete = c.appendPrescription(doctorid, patientid, dosage, notes, medicine);
        if(complete == true){
            JOptionPane.showMessageDialog(null, "Prescription of: "+medicine+
                    " For Patient: "+patientid+" Requested Successfully", "Success", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnCreatePrescriptActionPerformed

